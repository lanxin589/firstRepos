package day02;
/*
 * 2、有一个计算器，能够计算两个数字的和，三个数字的和，四个数字的和。五个数字的和
 */
public class test02_Caculator {
	public double add(double d1,double d2){
		return d1+d2;
	}
	public double add(double d1,double d2,double d3){
		return d1+d2+d3;
	}
	public double add(double d1,double d2,double d3,double d4){
		return d1+d2+d3+d4;
	}
	public double add(double d1,double d2,double d3,double d4,double d5){
		return d1+d2+d3+d4+d5;
	}

}
